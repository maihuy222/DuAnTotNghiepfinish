    <script>
        document.addEventListener('DOMContentLoaded', function() {
            document.querySelectorAll('.favorite-btn').forEach(function(btn) {
                btn.addEventListener('click', function() {
                    let productId = this.dataset.productId;
                    let button = this;

                    fetch("{{ route('favorites.toggle') }}", {
                            method: 'POST',
                            headers: {
                                'Content-Type': 'application/json',
                                'X-CSRF-TOKEN': "{{ csrf_token() }}"
                            },
                            body: JSON.stringify({
                                product_id: productId
                            })
                        })
                        .then(res => {
                            if (!res.ok) {
                                if (res.status === 401) {
                                    alert('Vui lòng đăng nhập để sử dụng tính năng này.');
                                    window.location.href = "{{ route('login') }}";
                                }
                                throw new Error(`HTTP error! status: ${res.status}`);
                            }
                            return res.json();
                        })
                        .then(data => {
                            if (data.status === 'added') {
                                button.classList.remove('btn-light');
                                button.classList.add('btn-danger');
                                button.title = 'Bỏ yêu thích';
                            } else if (data.status === 'removed') {
                                button.classList.remove('btn-danger');
                                button.classList.add('btn-light');
                                button.title = 'Thêm yêu thích';
                            }
                        })
                        .catch(err => console.error(err));
                });
            });
        });
    </script>



    <script>
        const searchInput = document.getElementById('searchInput');
        const autocompleteList = document.getElementById('autocompleteList');

        searchInput.addEventListener('input', function() {
            const keyword = this.value;

            if (keyword.length < 2) {
                autocompleteList.innerHTML = '';
                return;
            }

            fetch(`/search-autocomplete?query=${encodeURIComponent(keyword)}`)
                .then(res => res.json())
                .then(data => {
                    autocompleteList.innerHTML = '';

                    if (data.length === 0) {
                        autocompleteList.innerHTML = '<li class="list-group-item">Không có kết quả</li>';
                        return;
                    }

                    data.forEach(item => {
                        const li = document.createElement('li');
                        li.className = 'list-group-item list-group-item-action';
                        li.textContent = item;

                        li.addEventListener('click', function() {
                            searchInput.value = item;
                            autocompleteList.innerHTML = '';
                        });

                        autocompleteList.appendChild(li);
                    });
                });
        });

        // Click ngoài sẽ ẩn gợi ý
        document.addEventListener('click', function(e) {
            if (!searchInput.contains(e.target)) {
                autocompleteList.innerHTML = '';
            }
        });
    </script>



